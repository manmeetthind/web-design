function hideParagraph(){
    $("h1").hide(); // Hide Line 10
    $("#hideParagraph").hide(); // Hide Line 11
    $("#showParagraph").show(); // Show Line 12
}

function showParagraph(){
    $("h1").show();  // Show Line 10
    $("#hideParagraph").show(); // Show Line 11
    $("#showParagraph").hide(); // Hide Line 12
}